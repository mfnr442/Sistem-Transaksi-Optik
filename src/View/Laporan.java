    private void showTableLaporan() throws SQLException {
        int i = 0;
        DefaultTableModel dtmTra = new DefaultTableModel(new String[]{
            "Id Transaksi", "Id Karyawan", "Merk", "Jenis", "nama Pembeli",
            "No telp", "Tanggal Pembelian"
        }, 0);

        dtmTra.setRowCount(0);

        for (Transaksi t : this.con.getDataTransaksi()) {
            dtmTra.addRow(new String[]{
                t.getId_transaksi().toString(), t.getKaryawan().getId_karyawan().toString(),
                t.getarrDetail_Transaksi().get(i).getProduk().getMerk(),
                t.getarrDetail_Transaksi().get(i).getProduk().getJenis(),
                t.getPembeli().getNama_pembeli(),
                t.getPembeli().getNo_telp().toString(),
                t.getTanggal_pembelian().toString()

            });
            i++;
        }

        this.laporan.setModel(dtmTra);
    }

